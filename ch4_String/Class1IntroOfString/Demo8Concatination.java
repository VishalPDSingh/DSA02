package ch4_String.Class1IntroOfString;

public interface Demo8Concatination {
    public static void main(String[] args) {
        System.out.println(1+1);
        System.out.println("1"+1);
        System.out.println("1"+"1");
        System.out.println("1"+1+1);
        System.out.println(1+"1"+1);
        System.out.println(1+1+"1");
        
    }
}
